package com.example.scanner;

public enum TokenID {
	NUMBER,
	VARIABLE,
	CONSTANT,
	PI,
	E,	
	ADDITION,
	SUBTRACTION,
	MULTIPLICATION,
	DIVISION,
	POWER,
	SQRT,
	FACTORIAL,
	NEGATIVE,
	POSITIVE,
	FUNCTION,
	SIN,
	COS,
	TAN,
	LOG,
	COMMA,
	BRACKET,
	LEFT_BRACKET,
	RIGHT_BRACKET,
	TEXT,
	PARENT,
	MAT_INVERSE ,WORD,EQUAL, Operator, FRACTION, DERIVE, EXPONENTIAL, MATRIX, EMPTY, BASE;

	public boolean isFunction(){
		return equals(FUNCTION) || equals(DERIVE) || equals(LOG);
	}
}
